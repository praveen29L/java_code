public class for_demo {
    public static void main(String[] args) {
        int sum=0;
        for(int i=1;i<=10;i++){//intial state ,condition,update
            // System.out.println(i);
            if(i%2==0){
                sum=sum+i;
            }
        } System.out.println(sum);
    }
    
}
